'use client';

import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';
import { Invoice, formatCurrency } from '@/types';

export function generatePDF(invoice: Invoice): Blob {
  const doc = new jsPDF();

  // Sanitize all text fields to prevent jsPDF injection from HTML tags
  const clean = (s: string) => s.replace(/<[^\u003e]*>/g, '');
  const safeInvoice: Invoice = {
    ...invoice,
    number: clean(invoice.number),
    from: {
      ...invoice.from,
      name: clean(invoice.from.name),
      email: clean(invoice.from.email),
      phone: clean(invoice.from.phone),
      address: clean(invoice.from.address),
    },
    to: {
      ...invoice.to,
      name: clean(invoice.to.name),
      email: clean(invoice.to.email),
      phone: clean(invoice.to.phone),
      address: clean(invoice.to.address),
    },
    items: invoice.items.map(item => ({
      ...item,
      description: clean(item.description),
    })),
    notes: clean(invoice.notes),
    terms: clean(invoice.terms),
  };

  if (safeInvoice.template === 'modern') {
    generateModern(doc, safeInvoice);
  } else if (safeInvoice.template === 'classic') {
    generateClassic(doc, safeInvoice);
  } else if (safeInvoice.template === 'minimal') {
    generateMinimal(doc, safeInvoice);
  } else if (safeInvoice.template === 'clean') {
    generateClean(doc, safeInvoice);
  } else if (safeInvoice.template === 'bold') {
    generateBold(doc, safeInvoice);
  } else if (safeInvoice.template === 'corporate') {
    generateCorporate(doc, safeInvoice);
  } else if (safeInvoice.template === 'startup') {
    generateStartup(doc, safeInvoice);
  } else if (safeInvoice.template === 'freelancer') {
    generateFreelancer(doc, safeInvoice);
  } else if (safeInvoice.template === 'agency') {
    generateAgency(doc, safeInvoice);
  } else if (safeInvoice.template === 'consulting') {
    generateConsulting(doc, safeInvoice);
  } else {
    generateCreative(doc, safeInvoice);
  }

  return doc.output('blob');
}

function generateModern(doc: jsPDF, invoice: Invoice) {
  // Header gradient-ish band
  doc.setFillColor(59, 130, 246);
  doc.rect(0, 0, 210, 40, 'F');

  doc.setTextColor(255, 255, 255);
  doc.setFontSize(24);
  doc.setFont('helvetica', 'bold');
  doc.text('INVOICE', 15, 25);

  doc.setFontSize(12);
  doc.setFont('helvetica', 'normal');
  doc.text(`#${invoice.number}`, 15, 32);

  doc.setFontSize(10);
  doc.text(`Date: ${invoice.date}`, 160, 20);
  doc.text(`Due: ${invoice.dueDate}`, 160, 26);

  // From / To
  let y = 55;
  doc.setTextColor(31, 41, 55);
  doc.setFontSize(11);
  doc.setFont('helvetica', 'bold');
  doc.text('From:', 15, y);
  if (invoice.from.logo) {
    try { doc.addImage(invoice.from.logo, 'JPEG', 15, y + 2, 20, 20); } catch {}
  }
  doc.setFont('helvetica', 'normal');
  doc.text(invoice.from.name || 'Your Company', 15, y + 10);
  doc.text(invoice.from.email, 15, y + 15);
  doc.text(invoice.from.phone, 15, y + 20);
  doc.text(invoice.from.address, 15, y + 25);

  doc.setFont('helvetica', 'bold');
  doc.text('Bill To:', 110, y);
  doc.setFont('helvetica', 'normal');
  doc.text(invoice.to.name || 'Client Name', 110, y + 10);
  doc.text(invoice.to.email, 110, y + 15);
  doc.text(invoice.to.phone, 110, y + 20);
  doc.text(invoice.to.address, 110, y + 25);

  // Items table
  autoTable(doc, {
    startY: y + 35,
    head: [['Description', 'Qty', 'Rate', 'Amount']],
    body: invoice.items.map((item) => [
      item.description,
      String(item.quantity),
      formatCurrency(item.rate, invoice.currency),
      formatCurrency(item.quantity * item.rate, invoice.currency),
    ]),
    theme: 'striped',
    headStyles: { fillColor: [59, 130, 246], textColor: 255 },
    styles: { fontSize: 11 },
    foot: [
      ['', '', 'Subtotal', formatCurrency(subtotal(invoice), invoice.currency)],
      ['', '', `Tax (${invoice.taxRate}%)`, formatCurrency(taxAmount(invoice), invoice.currency)],
      ['', '', 'Total', formatCurrency(total(invoice), invoice.currency)],
    ],
    footStyles: { fillColor: [243, 244, 246], textColor: [31, 41, 55], fontStyle: 'bold' },
  });

  const finalY = (doc as any).lastAutoTable?.finalY || 180;
  if (invoice.notes) {
    doc.text('Notes:', 15, finalY + 15);
    doc.setFontSize(10);
    doc.text(invoice.notes, 15, finalY + 22);
  }
  if (invoice.terms) {
    doc.text(`Terms: ${invoice.terms}`, 15, finalY + 35);
  }

  doc.setFontSize(9);
  doc.setTextColor(156, 163, 175);
  doc.text('Generated by Billify', 15, 290);
}

